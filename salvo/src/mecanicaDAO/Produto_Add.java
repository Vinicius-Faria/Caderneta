package mecanicaDAO;

public class Produto_Add {
	
	
	private String Cod_Sistema;
	private String Cod_Barras;
	private String Descricao;
	private String Fornecedor;
	private String Data;
	private String Quantidade;
	private String preco_custo;
	private String preco_Venda;
	
	public String getCod_Sistema() {
		return Cod_Sistema;
	}
	public void setCod_Sistema(String cod_Sistema) {
		Cod_Sistema = cod_Sistema;
	}
	public String getCod_Barras() {
		return Cod_Barras;
	}
	public void setCod_Barras(String cod_Barras) {
		Cod_Barras = cod_Barras;
	}
	public String getDescricao() {
		return Descricao;
	}
	public void setDescricao(String descricao) {
		Descricao = descricao;
	}
	public String getFornecedor() {
		return Fornecedor;
	}
	public void setFornecedor(String fornecedor) {
		Fornecedor = fornecedor;
	}
	public String getData() {
		return Data;
	}
	public void setData(String data) {
		Data = data;
	}
	public String getQuantidade() {
		return Quantidade;
	}
	public void setQuantidade(String quantidade) {
		Quantidade = quantidade;
	}
	public String getPreco_custo() {
		return preco_custo;
	}
	public void setPreco_custo(String preco_custo) {
		this.preco_custo = preco_custo;
	}
	public String getPreco_Venda() {
		return preco_Venda;
	}
	public void setPreco_Venda(String preco_Venda) {
		this.preco_Venda = preco_Venda;
	}
	
	

}
