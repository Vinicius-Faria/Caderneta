package mecanicaDAO;

public class Contas_add {
	
	private String Cod_servico;
	private String Nome_cliente;
	private String Cpf_cliente;
	private String Placa_carro;
	private String Quant_prod;
	private String Valor_venda;
	
	public String getCod_servico() {
		return Cod_servico;
	}
	public void setCod_servico(String cod_servico) {
		Cod_servico = cod_servico;
	}
	public String getNome_cliente() {
		return Nome_cliente;
	}
	public void setNome_cliente(String nome_cliente) {
		Nome_cliente = nome_cliente;
	}
	public String getCpf_cliente() {
		return Cpf_cliente;
	}
	public void setCpf_cliente(String cpf_cliente) {
		Cpf_cliente = cpf_cliente;
	}
	public String getPlaca_carro() {
		return Placa_carro;
	}
	public void setPlaca_carro(String placa_carro) {
		Placa_carro = placa_carro;
	}
	public String getQuant_prod() {
		return Quant_prod;
	}
	public void setQuant_prod(String quant_prod) {
		Quant_prod = quant_prod;
	}
	public String getValor_venda() {
		return Valor_venda;
	}
	public void setValor_venda(String valor_venda) {
		Valor_venda = valor_venda;
	}

}
